package day03_EscapeSequences;
/*
escape sequences: MUST be given with double quote
    \n: start a new line
    \t: tab
    \\: Backslash which means: in order to print one backslash we need to give two backward slash
    \: double quote : in order to print double quote, we need to use \"

 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("------------------------------");

        System.out.println("Helo Talha \nHow are you today? \nIt's nice to meet you all! \nWhat class do we have ");

        System.out.println("------------------------------");

        System.out.println("\tJava is cool programming language");

        System.out.println("------------------------------");

        System.out.println("/ \\");

        System.out.println("---------------------");

        System.out.println("My favorite Tv show is \"Game og Thrones\"");

    }

}
