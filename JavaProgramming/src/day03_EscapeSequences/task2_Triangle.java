package day03_EscapeSequences;
/*
2. Create a class called Triangle.java and print:

                    ^
                   / \
                  /   \
                 /     \
                /       \
                ---------

 */
public class task2_Triangle {
    public static void main(String[] args) {
        System.out.println("\t    ^");
        System.out.println("\t   / \\");
        System.out.println("\t  /   \\");
        System.out.println("\t /     \\");
        System.out.println("\t/       \\");
        System.out.println("\t---------");
    }

}
